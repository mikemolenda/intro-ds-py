# Check Types
print('Check Types:')

def my_func():
    return None

print('type(\'string\'):\t{}'.format(str(type('string'))))
print('type(None):\t{}'.format(str(type(None))))
print('type(1):\t{}'.format(str(type(1))))
print('type(1.0):\t{}'.format(str(type(1.0))))
print('type(my_func):\t{}'.format(str(type(my_func))))
print('type([1, 2, 3]:\t{}'.format(str(type([1, 2, 3]))))
print('type((1, 2, 3):\t{}'.format(str(type((1, 2, 3)))))
print('type(\{1, 2, 3\}:\t{}'.format(str(type({1, 2, 3}))))

print()


